defmodule PCPMacros do
  @moduledoc """
  Even though Elixir attempts its best to provide a safe environment for macros,
  the major responsibility of writing clean code with macros falls on developers.
  Macros are harder to write than ordinary Elixir functions and it’s considered
  to be bad style to use them when they’re not necessary. So write macros responsibly.

  Elixir already provides mechanisms to write your everyday code in a simple and
  readable fashion by using its data structures and functions. Macros should
  only be used as a last resort. Remember that explicit is better than implicit.
  Clear code is better than concise code.

  https://elixir-lang.org/getting-started/meta/macros.html

  """

end
